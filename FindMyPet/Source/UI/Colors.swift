import Foundation

extension UIColor {
  
  class func avatarBorderColor() -> UIColor { return UIColor(rgbValue: 0xA5ABA9) }
  class func dividerColor() -> UIColor  { return UIColor(rgbValue: 0xf0f0f0) }
  class func placeholderTextColor() -> UIColor  { return UIColor(white: 0.8, alpha: 1) }
  class func navigationBarTintColor() -> UIColor  { return UIColor(rgbValue: 0x00C0D5) }
  class func darkTextColor() -> UIColor { return UIColor(rgbValue: 0x2e2e2e) }
  class func lightGreyTextColor() -> UIColor { return UIColor(rgbValue: 0xababab) }
  
}